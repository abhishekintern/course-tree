const groupHierarchy = (parent, children, key) => {
	return parent.map((p) => {
		p.children = [];
		children.forEach((child) => {
			if (child[key] === p._id) {
				delete child.termId;
				p.children.push(child);
			}
		});
		return p;
	});
};

